package trace

import (
	"math/rand"
	"time"
)

var letters = []rune("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789")

func RandSeq(n int) string {
	b := make([]rune, n)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	now := time.Now()
	pre := now.Format("20060102150405") // 或使用你的 constant.DTFormatNoSep
	return pre + string(b)
}
